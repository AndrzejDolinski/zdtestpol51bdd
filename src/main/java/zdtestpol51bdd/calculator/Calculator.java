package zdtestpol51bdd.calculator;

public class Calculator {
    public Integer addTwoNumbers(Integer a, Integer b) {
        return a + b;
    }

    public Integer subtractTwoNumbers(Integer a, Integer b) {
        return b - a;
    }

    public Integer divideTwoNumbers(Integer a, Integer b) {
        return a / b;
    }

    public Integer multiplyTwoNumbers(Integer a, Integer b) {
        return a * b;
    }
}
